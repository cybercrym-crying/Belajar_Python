# OPERASI LOGIKA

# AND, OR, XOR, NOT
print("AND")
a = True
b = True

print("TT", a and b)
a = True
b = False

print("TF", a and b)
a = False
b = True

print("FT", a and b)
a = False
b = False

print("FF", a and b)

print("OR")
a = True
b = True

print("TT", a or b)
a = True
b = False

print("TF", a or b)
a = False
b = True

print("FT", a or b)
a = False
b = False

print("FF", a or b)

print("XOR")
a = True
b = True

print("TT", a ^ b)
a = True
b = False

print("TF", a ^ b)
a = False
b = True

print("FT", a ^ b)
a = False
b = False

print("FF", a ^ b)
